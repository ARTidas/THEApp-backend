 * ********************************************************/
class MessageBo {

  protected $dao;

  /* ********************************************************
	 * ********************************************************
	 * ********************************************************/
  public function __construct() {
    $this->dao = new MessageDao(new MysqlDatabaseBo());
  }

  /* ********************************************************
	 * ********************************************************
	 * ********************************************************/
  public function getMessages() {
    return ($this->dao)->getMessages();
  }

  /* ********************************************************
	 * ********************************************************
	 * ********************************************************/
  public function createMessage(array $parameters) {
    LogHelper::add(implode('/', $parameters));
    return ($this->dao)->createMessage($parameters);
  }

}
?>
